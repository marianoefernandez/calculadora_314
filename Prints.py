def imprimir_resultados(numero_uno:int,numero_dos:int,resultado_suma:int,resultado_resta:int,resultado_multiplicacion:int,resultado_division:float | str,resultado_resto:int | str,resultado_potencia:int,resultado_factorial_A:int,resultado_factorial_B:int) -> None:
    print(f"1) {numero_uno} + {numero_dos} = {resultado_suma}")
    print(f"2) {numero_uno} - {numero_dos} = {resultado_resta}")
    print(f"3) {numero_uno} * {numero_dos} = {resultado_multiplicacion}")
    print(f"4) {numero_uno} / {numero_dos} = {resultado_division}")
    print(f"5) {numero_uno} / {numero_dos} tiene como resto = {resultado_resto}")
    print(f"6) {numero_uno} elevado a {numero_dos} = {resultado_potencia}")
    print(f"7) {numero_uno} ! = {resultado_factorial_A}")
    print(f"8) {numero_dos} ! = {resultado_factorial_B}")

